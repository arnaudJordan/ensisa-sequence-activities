package sequence.utilities;

/**
 * Defines the interface for scaleable classes.
 * 
 * @see Timeable
 * @see sequence.ui.utilities.TimeLayout
 */
public interface Scaleable {

	/**
	 * Gets the h scale.
	 * 
	 * @return the h scale
	 */
	public float getHScale();

	/**
	 * Gets the v scale.
	 * 
	 * @return the v scale
	 */
	public float getVScale();
}
